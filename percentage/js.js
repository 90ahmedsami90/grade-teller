let percentage=prompt("ENTER THE PERCENTAGE");

if(percentage > 100){
    alert("ERROR")
} else if(percentage >= 90){
    alert("A1 GRADE")
} else if(percentage >= 80){
    alert("A GRADE")
} else if(percentage >= 70){
    alert("B GRADE")
} else if(percentage >= 60){
    alert("C GRADE")
} else if(percentage < 60){
    alert("FAIL")
} else if(percentage <= 0){
    alert("ERROR")
}  else {
    alert("ERROR")
}
